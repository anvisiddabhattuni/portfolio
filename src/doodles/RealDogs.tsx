/**
 * Eyelid blink — lives OUTSIDE the breathing group so chest scaleY
 * doesn't compound with the lid and glitch in Chrome/SVG.
 * Hidden entirely while eyes are open (opacity 0) to avoid stray ovals.
 */
const Lid: React.FC<{ cx: number; cy: number; rx: number; ry: number; dur: number; delay: number; on: boolean }> =
  ({ cx, cy, rx, ry, dur, delay, on }) => (
  <motion.g
    style={{ transformOrigin: `${cx}px ${cy - ry}px`, transformBox }}
    initial={false}
    animate={
      on
        ? { scaleY: [0, 0, 1, 1, 0, 0], opacity: [0, 0, 1, 1, 0, 0] }
        : { scaleY: 0, opacity: 0 }
    }
    transition={{ duration: dur, repeat: Infinity, ease: 'linear', delay, times: blinkTimes }}
  >
    <ellipse cx={cx} cy={cy} rx={rx} ry={ry} fill={LID} />
  </motion.g>
);

/**
 * One dog, kept whole. The body + head are a single rigid group so the head
 * can never drift off the neck. Life comes from a planted chest breath (the
 * whole dog swells up from the floor), a tiny weight-shift sway, and blinking —
 * all anchored at the feet so the dog stays grounded, never floating.
 */
const Dog: React.FC<{ cfg: DogConfig; on: boolean }> = ({ cfg, on }) => (
  <motion.g
    initial={{ opacity: 0, y: 12 }}
    animate={{ opacity: 1, y: 0 }}
    transition={{
      opacity: { duration: 0.7, ease: [0.22, 1, 0.36, 1] },
      y: { duration: 0.9, ease: [0.22, 1, 0.36, 1] },
    }}
  >
    <motion.g
      style={{ transformOrigin: cfg.feet, transformBox }}
      animate={
        on
          ? { scaleY: cfg.breath.scaleY, scaleX: cfg.breath.scaleX }
          : { scaleY: 1, scaleX: 1 }
      }
      transition={{
        scaleY: { duration: cfg.breathDur, repeat: Infinity, ease: breathEase, delay: cfg.breathDelay },
        scaleX: { duration: cfg.breathDur, repeat: Infinity, ease: breathEase, delay: cfg.breathDelay },
      }}
    >
      <g dangerouslySetInnerHTML={{ __html: cfg.body }} />
      <g dangerouslySetInnerHTML={{ __html: cfg.head }} />
    </motion.g>
    {cfg.lids.map((l, i) => <Lid key={i} {...l} on={on} />)}
  </motion.g>
);

